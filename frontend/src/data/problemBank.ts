import type { CodingProblem } from '../types/coding';

export interface ProblemWithTestcases extends CodingProblem {
  visible_testcases: Array<{ input: string; expected_output: string }>;
  hidden_testcases: Array<{ input: string; expected_output: string }>;
}

function makeProblem(
  id: number,
  title: string,
  difficulty: 'Easy' | 'Medium' | 'Hard',
  topic: string,
  topic_tags: string[],
  description: string,
  input_format: string,
  output_format: string,
  constraints: string,
  examples: Array<{ input: string; output: string; explanation?: string }>,
  company_tags: string[],
  hints: string[],
  visible_testcases: Array<{ input: string; expected_output: string }>,
  hidden_testcases: Array<{ input: string; expected_output: string }>,
  is_premium = false,
): ProblemWithTestcases {
  return {
    id,
    title,
    difficulty,
    topic,
    tutorial_link: null,
    topic_tags,
    is_premium,
    description,
    input_format,
    output_format,
    constraints,
    examples,
    company_tags,
    hints,
    solved: false,
    is_bookmarked: false,
    visible_testcases,
    hidden_testcases,
  };
}

